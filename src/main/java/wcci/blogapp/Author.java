package wcci.blogapp;

public class Author {
	private String name;
	private String post;
	

	public String getName() {
		return name;
	}

	public String getPost() {
		return post;
	}

	

	public Author(String name, String post) {
		super();
		this.name = name;
		this.post = post;
		
}

	
}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
